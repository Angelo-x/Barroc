    {
        $order = new \App\Order();
        $order->user_id = \Auth::id();
        $order->product_id = $request->product_id;
        $order->save();

        $product = \App\Product::find($request->product_id);

        return (new \App\Mail\TestMail($product->name))->render();

    }

